package com.ctm.web.health.model.form;

public class GovtRebateDeclaration {

    private String applicantCovered;

    private String entitledToMedicare;

    private String govtRebateDeclaration;

    private String declarationDate;

    public String getApplicantCovered() {
        return applicantCovered;
    }

    public void setApplicantCovered(String applicantCovered) {
        this.applicantCovered = applicantCovered;
    }

    public String getEntitledToMedicare() {
        return entitledToMedicare;
    }

    public void setEntitledToMedicare(String entitledToMedicare) {
        this.entitledToMedicare = entitledToMedicare;
    }

    public String getGovtRebateDeclaration() {
        return govtRebateDeclaration;
    }

    public void setGovtRebateDeclaration(String govtRebateDeclaration) {
        this.govtRebateDeclaration = govtRebateDeclaration;
    }

    public String getDeclarationDate() {
        return declarationDate;
    }

    public void setDeclarationDate(String declarationDate) {
        this.declarationDate = declarationDate;
    }

}
