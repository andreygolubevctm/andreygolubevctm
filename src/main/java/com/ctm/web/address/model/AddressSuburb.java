package com.ctm.web.address.model;

public class AddressSuburb {
	  private String errorString;
    private String gnafId;
    private String houseNoSel;
    private String postCode;
    private String state;
    private String streetName;
    private String suburbName;
    private String text;
    private String unitSel;
    private String unitType;

	public String getErrorString() {
		return errorString;
	}

	public void setErrorString(String errorString) {
		this.errorString = errorString;
  }

  public String getGnafId() {
		return gnafId;
	}

	public void setGnafId(String gnafId) {
		this.gnafId = gnafId;
  }

  public String getHouseNoSel() {
		return houseNoSel;
	}

	public void setHouseNoSel(String houseNoSel) {
		this.houseNoSel = houseNoSel;
  }

  public String getPostCode() {
		return postCode;
	}

	public void setPostCode(String postCode) {
		this.postCode = postCode;
  }

  public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
  }

  public String getStreetName() {
		return streetName;
	}

	public void setStreetName(String streetName) {
		this.streetName = streetName;
  }

  public String getSuburbName() {
		return suburbName;
  }

	public void setSuburbName(String suburbName) {
		this.suburbName = suburbName;
  }

  public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
  }

  public String getUnitSel() {
		return unitSel;
	}

	public void setUnitSel(String unitSel) {
		this.unitSel = unitSel;
  }

  public String getUnitType() {
		return unitType;
	}

	public void setUnitType(String unitType) {
		this.unitType = unitType;
  }
}
