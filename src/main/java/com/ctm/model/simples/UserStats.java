package com.ctm.model.simples;

public class UserStats {
	private int completed;
	private int completedAsPM;
	private int unsuccessful;
	private int postponed;
	private double contact;
	private int sales;
	private double conversion;
	private int hot;
	private int remaining;
	private int future;

	public int getCompleted() {
		return completed;
	}
	public void setCompleted(int completed) {
		this.completed = completed;
	}
	public int getCompletedAsPM() {
		return completedAsPM;
	}
	public void setCompletedAsPM(int completedAsPM) {
		this.completedAsPM = completedAsPM;
	}
	public int getUnsuccessful() {
		return unsuccessful;
	}
	public void setUnsuccessful(int unsuccessful) {
		this.unsuccessful = unsuccessful;
	}

	public int getPostponed() {
		return postponed;
	}
	public void setPostponed(int postponed) {
		this.postponed = postponed;
	}

	public double getContact() {
		return contact;
	}
	public void setContact(double contact) {
		this.contact = contact;
	}
	public int getSales() {
		return sales;
	}
	public void setSales(int sales) {
		this.sales = sales;
	}
	public double getConversion() {
		return conversion;
	}
	public void setConversion(double conversion) {
		this.conversion = conversion;
	}
	public int getHot() {
		return hot;
	}
	public void setHot(int hot) {
		this.hot = hot;
	}
	public int getRemaining() {
		return remaining;
	}
	public void setRemaining(int remaining) {
		this.remaining = remaining;
	}
	public int getFuture() {
		return future;
	}
	public void setFuture(int future) {
		this.future = future;
	}

}
