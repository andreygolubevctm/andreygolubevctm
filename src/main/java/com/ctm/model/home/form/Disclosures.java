package com.ctm.model.home.form;

public class Disclosures {

    private String atCurrentAddress;

    private String claims;

    private String coverLength;

    private String expiry;

    private String expiryInputD;

    private String expiryInputM;

    private String expiryInputY;

    private String insurer;

    private String previousInsurance;

    public String getAtCurrentAddress() {
        return atCurrentAddress;
    }

    public void setAtCurrentAddress(String atCurrentAddress) {
        this.atCurrentAddress = atCurrentAddress;
    }

    public String getClaims() {
        return claims;
    }

    public void setClaims(String claims) {
        this.claims = claims;
    }

    public String getCoverLength() {
        return coverLength;
    }

    public void setCoverLength(String coverLength) {
        this.coverLength = coverLength;
    }

    public String getExpiry() {
        return expiry;
    }

    public void setExpiry(String expiry) {
        this.expiry = expiry;
    }

    public String getExpiryInputD() {
        return expiryInputD;
    }

    public void setExpiryInputD(String expiryInputD) {
        this.expiryInputD = expiryInputD;
    }

    public String getExpiryInputM() {
        return expiryInputM;
    }

    public void setExpiryInputM(String expiryInputM) {
        this.expiryInputM = expiryInputM;
    }

    public String getExpiryInputY() {
        return expiryInputY;
    }

    public void setExpiryInputY(String expiryInputY) {
        this.expiryInputY = expiryInputY;
    }

    public String getInsurer() {
        return insurer;
    }

    public void setInsurer(String insurer) {
        this.insurer = insurer;
    }

    public String getPreviousInsurance() {
        return previousInsurance;
    }

    public void setPreviousInsurance(String previousInsurance) {
        this.previousInsurance = previousInsurance;
    }
}
