package com.ctm.model.home.form;

public class SecurityFeatures {

    private String externalSiren;

    private String internalSiren;

    private String strobeLight;

    private String backToBase;

    public String getExternalSiren() {
        return externalSiren;
    }

    public void setExternalSiren(String externalSiren) {
        this.externalSiren = externalSiren;
    }

    public String getInternalSiren() {
        return internalSiren;
    }

    public void setInternalSiren(String internalSiren) {
        this.internalSiren = internalSiren;
    }

    public String getStrobeLight() {
        return strobeLight;
    }

    public void setStrobeLight(String strobeLight) {
        this.strobeLight = strobeLight;
    }

    public String getBackToBase() {
        return backToBase;
    }

    public void setBackToBase(String backToBase) {
        this.backToBase = backToBase;
    }
}
