package com.ctm.model.health.form;

public class Address {

    private String type;

    private String streetNum;

    private String houseNoSel;

    private String streetName;

    private String suburbName;

    private String fullAddressLineOne;

    private String postCode;

    private String dpId;

    private String state;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getStreetNum() {
        return streetNum;
    }

    public void setStreetNum(String streetNum) {
        this.streetNum = streetNum;
    }

    public String getHouseNoSel() {
        return houseNoSel;
    }

    public void setHouseNoSel(String houseNoSel) {
        this.houseNoSel = houseNoSel;
    }

    public String getStreetName() {
        return streetName;
    }

    public void setStreetName(String streetName) {
        this.streetName = streetName;
    }

    public String getSuburbName() {
        return suburbName;
    }

    public void setSuburbName(String suburbName) {
        this.suburbName = suburbName;
    }

    public String getFullAddressLineOne() {
        return fullAddressLineOne;
    }

    public void setFullAddressLineOne(String fullAddressLineOne) {
        this.fullAddressLineOne = fullAddressLineOne;
    }

    public String getPostCode() {
        return postCode;
    }

    public void setPostCode(String postCode) {
        this.postCode = postCode;
    }

    public String getDpId() {
        return dpId;
    }

    public void setDpId(String dpId) {
        this.dpId = dpId;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }
}
