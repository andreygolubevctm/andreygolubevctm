package com.ctm.model.simples;

public class UserStats {
	private int completed;
	private int unsuccessful;
	private int postponed;
	private int contact;
	private int sales;
	private float conversion;
	private int active;
	private int future;

	public int getCompleted() {
		return completed;
	}
	public void setCompleted(int completed) {
		this.completed = completed;
	}

	public int getUnsuccessful() {
		return unsuccessful;
	}
	public void setUnsuccessful(int unsuccessful) {
		this.unsuccessful = unsuccessful;
	}

	public int getPostponed() {
		return postponed;
	}
	public void setPostponed(int postponed) {
		this.postponed = postponed;
	}

	public int getContact() {
		return contact;
	}
	public void setContact(int contact) {
		this.contact = contact;
	}
	public int getSales() {
		return sales;
	}
	public void setSales(int sales) {
		this.sales = sales;
	}
	public float getConversion() {
		return conversion;
	}
	public void setConversion(float conversion) {
		this.conversion = conversion;
	}
	public int getActive() {
		return active;
	}
	public void setActive(int active) {
		this.active = active;
	}
	public int getFuture() {
		return future;
	}
	public void setFuture(int future) {
		this.future = future;
	}

}
